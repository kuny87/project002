package com.next.sample.model;

public class ParsingBoard {

	private Integer parsingBoardSeq;
	private String ranking;
	private String name;
	private String tear;
	private String lp;
	private String lv;
	private String shift;
	
	public Integer getParsingBoardSeq() {
		return parsingBoardSeq;
	}
	public void setParsingBoardSeq(Integer parsingBoardSeq) {
		this.parsingBoardSeq = parsingBoardSeq;
	}
	public String getRanking() {
		return ranking;
	}
	public void setRanking(String ranking) {
		this.ranking = ranking;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTear() {
		return tear;
	}
	public void setTear(String tear) {
		this.tear = tear;
	}
	public String getLp() {
		return lp;
	}
	public void setLp(String lp) {
		this.lp = lp;
	}
	public String getLv() {
		return lv;
	}
	public void setLv(String lv) {
		this.lv = lv;
	}
	public String getShift() {
		return shift;
	}
	public void setShift(String shift) {
		this.shift = shift;
	}

}
